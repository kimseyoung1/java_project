package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {

		// 비교연산자: ==, !=, >, >=, <, <=
		// 비교결과가 boolean값(논리데이터)으로 출력됨.
		int x = 100;
		int y = 200;
		
		System.out.println(x == y);
		System.out.println(x != y);
		System.out.println(x >= y);
		System.out.println(x > y);
		System.out.println(x <= y);
		System.out.println(x < y);
	}
}